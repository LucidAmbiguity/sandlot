        // dd($roleName, $user);
        return back();
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id, Request $request)
    {
        $this->validate($request, [
            'userId' => 'required',
        ]);
        // dd($id, $request->userId);
        $user = User::find($request->userId);
        // dd($user);
        $user->removeRole($id);
        return back();
    }
